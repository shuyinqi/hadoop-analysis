package com.mapbar.analyzelog.model;

public class Event {
	private String t;
	private int sid;
	private String eid;
	private String lab;
	private int acc;
	
	public final String getT() {
		return t;
	}
	public final void setT(String t) {
		this.t = t;
	}
	public final int getSid() {
		return sid;
	}
	public final void setSid(int sid) {
		this.sid = sid;
	}
	public final String getEid() {
		return eid;
	}
	public final void setEid(String eid) {
		this.eid = eid;
	}
	public final String getLab() {
		return lab;
	}
	public final void setLab(String lab) {
		this.lab = lab;
	}
	public final int getAcc() {
		return acc;
	}
	public final void setAcc(int acc) {
		this.acc = acc;
	}
	
	
}
