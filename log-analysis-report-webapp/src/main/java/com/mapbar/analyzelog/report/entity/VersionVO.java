package com.mapbar.analyzelog.report.entity;

public class VersionVO {
	private String version;
	private String counts;
	private String news;
	private String ups;
	private String todays;
	private String yesterdays;
	private String vratio;
	public final String getVersion() {
		return version;
	}
	public final void setVersion(String version) {
		this.version = version;
	}
	public final String getCounts() {
		return counts;
	}
	public final void setCounts(String counts) {
		this.counts = counts;
	}
	public final String getNews() {
		return news;
	}
	public final void setNews(String news) {
		this.news = news;
	}
	public final String getUps() {
		return ups;
	}
	public final void setUps(String ups) {
		this.ups = ups;
	}
	public final String getTodays() {
		return todays;
	}
	public final void setTodays(String todays) {
		this.todays = todays;
	}
	public final String getYesterdays() {
		return yesterdays;
	}
	public final void setYesterdays(String yesterdays) {
		this.yesterdays = yesterdays;
	}
	public final String getVratio() {
		return vratio;
	}
	public final void setVratio(String vratio) {
		this.vratio = vratio;
	}
	
}
